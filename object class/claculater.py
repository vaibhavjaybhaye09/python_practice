 @classmethod
 def add(cls,a,b):
     return f"its addition ={a+b}"
 def sub(cls,a,b):
     return f"its addition ={a-b}"
 def mulp(cls,a,b):
     return f"its addition ={a*b}"
 def div(cls,a,b):
    return f"its addition ={a/b}"

